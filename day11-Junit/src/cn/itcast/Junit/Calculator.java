package cn.itcast.Junit;
/**
 * 计算器类
 * */
public class Calculator {
    /**
     * 加法运算
     * @param a
     * @param b
     * @return
     */
    public int add(int a,int b){

        return a+b;
    }

    /**
     * 减法运算
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b){
        return a-b;
    }
}
